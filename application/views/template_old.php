                    function recursiveMenu($sourceArr,$parents = 0,&$newMenu){
                        if(count($sourceArr)>0){
                            $idUL = 'ulGroup_' . $parents;
                            $newMenu .= '<ul id="'.$idUL.'">';
                            $i=0;
                            foreach ($sourceArr as $key => $value){
                                if($value['parents'] == $parents){
                                    $liMenu = 'liMenu_' . $value['id'];
                                    if(!empty($value['url'])){
                                        $strUrl = base_url().$value['url'];
                                    }else{
                                        $strUrl = "#";
                                    }
                                    
                                    $newMenu .= '<li id="'. $liMenu . '"><a href="' .$strUrl . '" title="'.$value['name'].'" class="b_menu">'
                                            . '<i class="fa fa-table"></i> 
                                              <span class="link-title">' . $value['name'] . '</span></a>';				
                                    $newParents = $value['id'];
                                    unset($sourceArr[$key]);
                                    recursiveMenu($sourceArr,$newParents, $newMenu);
                                    $newMenu .= '</li>';
                                    $i++;
                                    $newMenu =  str_replace('<ul id="'.$idUL.' ">','',$newMenu);
                                }

                            }
                            $newMenu .= '</ul>';
                        }
